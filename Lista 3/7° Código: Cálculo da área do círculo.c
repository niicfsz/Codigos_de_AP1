#include <stdio.h>
#include <math.h>

int main(){

//declarando variáveis
    float r, area, r2;

//solicitando ao usuário as informações da circunferência
    printf("Digite o raio da circunferencia: ");
    scanf("%f", &r);

//calculando a área
    r2 = r*r;
    area = M_PI * r2;

//exibindo o resultado na tela
    printf("\n\nA area da sua circunferencia eh: %.2f\n", area);
}
